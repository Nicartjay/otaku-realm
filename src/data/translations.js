/**
 * Returns the translated text for a key given the current language.
 * If lang is 'en', returns the enOverride (if one exists) or the key itself.
 * If lang is 'jp', returns the Japanese translation or falls back to the key.
 */
export function t(key, lang) {
  if (lang === 'jp' && translations[key]) return translations[key]
  if (lang === 'en' && enOverrides[key]) return enOverrides[key]
  return key
}
